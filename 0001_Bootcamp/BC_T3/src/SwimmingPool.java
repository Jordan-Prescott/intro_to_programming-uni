
public class SwimmingPool {
	
	public static void main(String [] args)
	{
		int length = 25;
		int width = 10;
		int depth = 2;
		
		System.out.println(
				"A pool of length: " + length + "m, width " + width + "m and depth " + depth + "m will require " + ((length * width * depth) * 1000) + " litres of water to fill it."
				);
		
	}

}
